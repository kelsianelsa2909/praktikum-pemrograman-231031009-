# mendefinisikan variabel a dengan nilai boolean True.
a = True
count = 0

#while a::memulai loop while yang akan berjalan selama  nilai variabel a adalah True.
while a:
        print('Menjalankan Program')
        count += 1
        if count == 5:
            break

#`count += 1`: Menambah penghitung sebanyak 1 di setiap iterasi.
# `jika hitungan == 5: istirahat`: Memeriksa apakah penghitung telah mencapai 5 dan keluar dari perulangan jika benar.
